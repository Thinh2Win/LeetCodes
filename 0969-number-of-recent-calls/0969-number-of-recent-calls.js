
var RecentCounter = function() {
    this.queue = [];
};

/** 
 * @param {number} t
 * @return {number}
 */
RecentCounter.prototype.ping = function(t) {
    this.queue.unshift(t);
    let end = t - 3000;
    while (this.queue.at(-1) < end) {
        this.queue.pop();
    } 
    return this.queue.length;
};

